#!/bin/bash

# Вводим переменную n
read -p "Введите значение n: " n

# Базовый массив
base_array=(1 2 4 6)
result=()

# Начинаем с добавления первого базового массива
result+=("${base_array[@]}")

for ((i = 1; i <= n; i++)); do
    temp=() # Временный массив для хранения новых элементов
    for num in "${base_array[@]}"; do
        temp+=($((num + 9 * i))) # Вычисляем новые элементы
    done
    result+=("${temp[@]}") # Правильно добавляем элементы в общий массив
done

# Выводим итоговый массив
echo "${result[@]}"